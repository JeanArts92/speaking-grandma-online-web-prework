    puts "I LOVE YOU TOO PUMPKIN!"
    elsif string = "Hi nana, how are you?"
    puts "HUH!? SPEAK UP SUNNY!"
    elsif string = "WHAT DID YOU EAT TODAY"
    puts "NO, NOT SINCE 1938!"
